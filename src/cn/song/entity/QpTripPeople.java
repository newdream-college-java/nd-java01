package cn.song.entity;

public class QpTripPeople {
	private String qtpName;
	private String qtpPhone;
	private String qtCardNo;
	private int orderId;
	public String getQtpName() {
		return qtpName;
	}
	public void setQtpName(String qtpName) {
		this.qtpName = qtpName;
	}
	
	public String getQtCardNo() {
		return qtCardNo;
	}
	public void setQtCardNo(String qtCardNo) {
		this.qtCardNo = qtCardNo;
	}
	public String getQtpPhone() {
		return qtpPhone;
	}
	public void setQtpPhone(String qtpPhone) {
		this.qtpPhone = qtpPhone;
	}
	public int getOrderId() {
		return orderId;
	}
	public void setOrderId(int orderId) {
		this.orderId = orderId;
	}
	
}
