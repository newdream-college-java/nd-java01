package cn.song.entity;

public class TripOrder {
	private int toId;
	private String toNumber;
	private int toTicketId;
	private int isUseCoupon;
	private int toCouponPrice;
	private int toCharge;
	private String toTime;
	private int toStatus;
	private int toUserId;
	private int toNum;
	private int toPrice;
	private String issureNo;
	public int getToId() {
		return toId;
	}
	public void setToId(int toId) {
		this.toId = toId;
	}
	public String getToNumber() {
		return toNumber;
	}
	public void setToNumber(String toNumber) {
		this.toNumber = toNumber;
	}
	public int getToTicketId() {
		return toTicketId;
	}
	public void setToTicketId(int toTicketId) {
		this.toTicketId = toTicketId;
	}
	public int getIsUseCoupon() {
		return isUseCoupon;
	}
	public void setIsUseCoupon(int isUseCoupon) {
		this.isUseCoupon = isUseCoupon;
	}
	public int getToCouponPrice() {
		return toCouponPrice;
	}
	public void setToCouponPrice(int toCouponPrice) {
		this.toCouponPrice = toCouponPrice;
	}
	public int getToCharge() {
		return toCharge;
	}
	public void setToCharge(int toCharge) {
		this.toCharge = toCharge;
	}
	public String getToTime() {
		return toTime;
	}
	public void setToTime(String toTime) {
		this.toTime = toTime;
	}
	public int getToStatus() {
		return toStatus;
	}
	public void setToStatus(int toStatus) {
		this.toStatus = toStatus;
	}
	public int getToUserId() {
		return toUserId;
	}
	public void setToUserId(int toUserId) {
		this.toUserId = toUserId;
	}
	public int getToNum() {
		return toNum;
	}
	public void setToNum(int toNum) {
		this.toNum = toNum;
	}
	public int getToPrice() {
		return toPrice;
	}
	public void setToPrice(int toPrice) {
		this.toPrice = toPrice;
	}
	public String getIssureNo() {
		return issureNo;
	}
	public void setIssureNo(String issureNo) {
		this.issureNo = issureNo;
	}
	
}
