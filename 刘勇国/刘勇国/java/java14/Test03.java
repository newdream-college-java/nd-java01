package 第14章封装作业;

import java.util.Scanner;

public class Test03 {
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		System.out.println("欢迎您来到宠物店！");
		System.out.print("请输入要领养宠物的名字：");
		String petName=input.next();
		System.out.print("请选择要领养的宠物类型：（1.聪明的拉布拉多犬2.酷酷的雪娜瑞3.猪）");
		int petType = input.nextInt();
		if(petType==1){
			Dog03 dog=new Dog03();
			dog.setName(petName);
			System.out.println("宠物的自白:");
			dog.printMessage();
		}else if(petType==3){
			Pig03 pig = new Pig03(100,80);
			pig.setName(petName);
			System.out.println("请输入宠物的颜色(1.白2.黑)");
			int color=input.nextInt();
			if(color==1){
				pig.setColor("白");
			}else{
				pig.setColor("黑");
			}
			pig.show();
		}else{
			Penguin03 penguin = new Penguin03(petName,99,80);
			System.out.println("请选择企鹅的性别：（1.Q仔2.Q妹）");
			int chioce=input.nextInt();
			if(chioce==1){
				penguin.setSex(penguin.sex_male);
			}else{
				penguin.setSex(penguin.sex_female);
			}
			//penguin.setName(petName);
			//penguin.set
			System.out.println("宠物的自白:");
			penguin.show();
		}
	}
}

