
public class Calculator {
	double num1;
	double num2;
	double sum=0;
	public double showAdd(){
		sum=num1+num2;
		return sum;
	}
	public double showMinus(){
		sum=num1-num2;
		return sum;
	}
	public double showMultiple(){
		sum=num1*num2;
		return sum;
	}
	public double showDivide(){
		sum=num1/num2;
		return sum;
	}
}
