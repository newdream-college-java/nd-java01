package com.java.java17.Z1;

public abstract class Programme {
	private String name;
	private String zy;
	public abstract void show();
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getZy() {
		return zy;
	}
	public void setZy(String zy) {
		this.zy = zy;
	}
	
}
